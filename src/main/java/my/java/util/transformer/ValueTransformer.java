package my.java.util.transformer;

/**
 * Интерфейс для трансформации значений из строк в нужный тип
 * @param <T> тип, в который будет преобразовано значение
 */
public interface ValueTransformer<T> {

    /**
     * Преобразует строковое значение в заданный тип
     *
     * @param value строковое значение для преобразования
     * @param params дополнительные параметры для преобразования (например, формат даты)
     * @return преобразованное значение или null, если преобразование невозможно
     */
    T transform(String value, String params);

    /**
     * Проверяет, возможно ли преобразование строки в заданный тип
     *
     * @param value строковое значение для проверки
     * @param params дополнительные параметры
     * @return true, если преобразование возможно
     */
    boolean canTransform(String value, String params);

    /**
     * Возвращает тип, в который преобразуется значение
     *
     * @return класс типа
     */
    Class<T> getTargetType();

    /**
     * Возвращает строковое представление значения
     *
     * @param value значение для преобразования в строку
     * @param params дополнительные параметры (например, формат даты)
     * @return строковое представление значения
     */
    String toString(T value, String params);
}